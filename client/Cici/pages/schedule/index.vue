const getEventDuration = (event) => {
  // Â¶ÇÊûúÈÄâÊã©‰∫ÜÂÖ®Â§©Â§çÈÄâÊ°ÜÔºåÂàôÊòæÁ§∫"ÂÖ®Â§©"
  if (event.allDay === true) {
    return 'ÂÖ®Â§©'
  }
  
  // Â¶ÇÊûúÊúâÂºÄÂßãÊó∂Èó¥ÂíåÁªìÊùüÊó∂Èó¥ÔºåËÆ°ÁÆóÊåÅÁª≠Êó∂Èó¥
  if (event.startTime && event.endTime) {
    // Ëß£ÊûêÊó∂Èó¥Â≠óÁ¨¶‰∏≤ (Ê†ºÂºè: "HH:mm")
    const startParts = event.startTime.split(':')
    const endParts = event.endTime.split(':')
    
    if (startParts.length === 2 && endParts.length === 2) {
      const startHour = parseInt(startParts[0])
      const startMinute = parseInt(startParts[1])
      const endHour = parseInt(endParts[0])
      const endMinute = parseInt(endParts[1])
      
      // ËÆ°ÁÆóÊåÅÁª≠Êó∂Èó¥ÔºàÂàÜÈíüÔºâ
      const startTotalMinutes = startHour * 60 + startMinute
      const endTotalMinutes = endHour * 60 + endMinute
      let durationMinutes = endTotalMinutes - startTotalMinutes
      
      // Â§ÑÁêÜË∑®Â§©ÁöÑÊÉÖÂÜµÔºàÂ¶ÇÊûúÁªìÊùüÊó∂Èó¥Â∞è‰∫éÂºÄÂßãÊó∂Èó¥Ôºâ
      if (durationMinutes < 0) {
        durationMinutes += 24 * 60 // Âä†‰∏ä24Â∞èÊó∂
      }
      
      // ËΩ¨Êç¢‰∏∫Â∞èÊó∂ÂíåÂàÜÈíü
      const durationHours = Math.floor(durationMinutes / 60)
      const remainingMinutes = durationMinutes % 60
      
      // Ê†ºÂºèÂåñÊòæÁ§∫ÊåÅÁª≠Êó∂Èó¥ÔºàÊó∂:ÂàÜÊ†ºÂºèÔºâ
      if (durationHours > 0 && remainingMinutes > 0) {
